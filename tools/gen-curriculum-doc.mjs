// tools/gen-curriculum-doc.mjs — generate docs/CURRICULUM.md from the live
// curriculum + standards data, so the scope & sequence is always accurate.
// Run with `npm run docs:curriculum`.
import { writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';
import { BY_GRADE, STRANDS, getStandard, ALL_SKILLS, standardsCount } from '../js/curriculum/index.js';

const here = dirname(fileURLToPath(import.meta.url));
const out = join(here, '..', 'docs', 'CURRICULUM.md');

let md = `# MathQuest Curriculum Map

> **Auto-generated** by \`npm run docs:curriculum\` from the live curriculum data.
> Do not edit by hand — edit \`js/curriculum/grade*.js\` and \`js/curriculum/standards.js\`, then re-run.

**${ALL_SKILLS.length} skills · ${standardsCount} Common Core standards · Grades 3–6**

Every skill below is generated by the teaching engine (infinite practice problems,
step-by-step worked solutions, and a 3-step hint ladder) and aligned to the Common Core
State Standards for Mathematics.

`;

for (const grade of [3, 4, 5, 6]) {
  const skills = BY_GRADE[grade];
  md += `\n## Grade ${grade}  ·  ${skills.length} skills\n\n`;
  for (const strand of STRANDS) {
    const inStrand = skills.filter((s) => s.strand === strand);
    if (!inStrand.length) continue;
    md += `### ${strand}\n\n`;
    md += `| Skill | Standard | What it means |\n|---|---|---|\n`;
    for (const s of inStrand) {
      const st = getStandard(s.id);
      const code = st ? st.code : '—';
      const text = st ? st.text.replace(/\|/g, '\\|') : '';
      md += `| ${s.emoji || ''} ${s.title} | \`${code}\` | ${text} |\n`;
    }
    md += `\n`;
  }
}

md += `\n---\n\n*Aligned to the [Common Core State Standards for Mathematics](https://www.thecorestandards.org/Math/). `;
md += `MathQuest is private by design — all progress stays on the device, with no accounts, ads, or tracking.*\n`;

writeFileSync(out, md);
console.log(`Wrote ${out} (${ALL_SKILLS.length} skills, ${standardsCount} standards).`);
